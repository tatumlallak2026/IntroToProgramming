x = input("enter 1st number: ")
y = input("enter 2nd number: ")
#reciving data from user
opperation = input("enter operation")
#asking for opperation
if opperation == "+":
    print (int(x)+int(y))
elif opperation == "-":
    print (int(x)-int(y))
elif opperation == "*":
    print (int(x)*int(y))
elif opperation == "/":
    print (int(x)/int(y))
